/**
 * Render text as pixel art
 */
export function renderPixelText(
  text: string,
  colors: PaletteColors,
  app: Application,
  scale = 2,
  spacing = 1
): RenderTexture {
  const upperText = text.toUpperCase();
  const graphics = new Graphics();

  let xOffset = 0;
  const charWidth = 5;
  const charHeight = 7;

  for (let i = 0; i < upperText.length; i++) {
    const char = upperText[i];
    const charData = FONT_DATA[char];

    if (!charData) {
      // Skip unknown characters, but advance position
      xOffset += (charWidth + spacing) * scale;
      continue;
    }

    // Draw character
    for (let y = 0; y < charHeight; y++) {
      for (let x = 0; x < charWidth; x++) {
        if (charData[y][x] === 1) {
          graphics.rect(xOffset + x * scale, y * scale, scale, scale);
          graphics.fill({ color: colors.foreground });
        }
      }
    }

    xOffset += (charWidth + spacing) * scale;
  }

  // Calculate total width and height
  const totalWidth = xOffset;
  const totalHeight = charHeight * scale;

  // Create render texture
  const texture = RenderTexture.create({
    width: totalWidth,
    height: totalHeight,
  });

  app.renderer.render({
    container: graphics,
    target: texture,
  });

  graphics.destroy();
  return texture;
}

/**
 * Calculate the width of rendered text
 */
export function calculateTextWidth(text: string, scale = 2, spacing = 1): number {
  const charWidth = 5;
  return text.length * (charWidth + spacing) * scale;
}

/**
 * Calculate the height of rendered text
 */
export function calculateTextHeight(scale = 2): number {
  const charHeight = 7;
  return charHeight * scale;
}

/**
 * Get available characters in font
 */
export function getAvailableCharacters(): string[] {
  return Object.keys(FONT_DATA);
}
